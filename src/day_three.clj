(defn count-trees
  [xs]
  (loop [tree-count 0
         pos {:x 0 :y 0}]
    (let [[d row] (get xs (:y pos))
          x (mod (:x pos) (if (nil? row) 1 (count row)))
          current-char (get row x)]
      (if (nil? row) tree-count
          (recur
           (if (= current-char \#) (inc tree-count) tree-count)
           (merge-with + pos d))))))

(defn inspect
